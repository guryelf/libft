#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*val;

	val = NULL;
	if (s == NULL)
		return (NULL);
	if (c > 255)
		return ((char *)s);
	while (*s++)
	{
		if (*s == c)
			val = (char *)s;
	}
	return (val);
}
